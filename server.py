@app.route("/analyze", methods=["POST"])
def analyze():
    data = request.get_json()

    return jsonify({
        "analysis": build_analysis(data),
        "variants": get_variants(data.get("risk")),
        "scenarios": get_scenarios(data.get("risk")),
        "pro_offer": {
            "title": "Нужно больше вариантов?",
            "text": "В PRO-версии доступны расширенные сценарии и больше портфелей."
        }
    })


