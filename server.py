import random

def generate_analysis(data):
    risk = data.get("risk", "сбалансированный")
    horizon = data.get("horizon", "6 месяцев")

    market_states = [
        "рынок находится в фазе умеренного роста",
        "наблюдается повышенная волатильность",
        "рынок демонстрирует смешанную динамику",
        "идёт коррекция после роста"
    ]

    sectors = [
        "технологический сектор",
        "энергетика",
        "финансовый сектор",
        "сырьевые активы"
    ]

    returns = ["5–8%", "6–10%", "4–7%", "7–12%"]

    text = f"""
Анализ рынка:
Сейчас {random.choice(market_states)}. При этом {random.choice(sectors)} показывает более активное движение.

Потенциал стратегии:
На горизонте {horizon} ожидаемая доходность может составлять {random.choice(returns)} годовых при умеренном уровне риска.

Почему это решение подходит:
Портфель соответствует профилю «{risk}» и сочетает защитные и растущие активы, что помогает адаптироваться к текущей ситуации.

Риски:
Возможны краткосрочные просадки из-за макроэкономических факторов и новостного фона.
"""
    return text
