from flask import Flask, request, jsonify
from flask_cors import CORS
import random

try:
    import yfinance as yf
except Exception:
    yf = None

app = Flask(__name__)
CORS(app)


def get_market_data():
    if yf is None:
        return {
            "sp500": "н/д",
            "oil": "н/д",
            "gold": "н/д"
        }

    try:
        sp500 = yf.Ticker("^GSPC").history(period="1d")
        oil = yf.Ticker("CL=F").history(period="1d")
        gold = yf.Ticker("GC=F").history(period="1d")

        return {
            "sp500": round(float(sp500["Close"].iloc[-1]), 2),
            "oil": round(float(oil["Close"].iloc[-1]), 2),
            "gold": round(float(gold["Close"].iloc[-1]), 2)
        }
    except Exception:
        return {
            "sp500": "н/д",
            "oil": "н/д",
            "gold": "н/д"
        }


def get_market_mood():
    return random.choice([
        "рынок показывает смешанную динамику",
        "рынок находится в фазе умеренной волатильности",
        "рынок выглядит осторожно, но сохраняет точки входа",
        "инвесторы сохраняют интерес к защитным и диверсифицированным стратегиям"
    ])


def build_analysis(data, market):
    name = data.get("name", "инвестора")
    term = data.get("term", "не указан")
    amount = data.get("amount", "не указана")
    goal = data.get("goal", "не указана")
    risk = data.get("risk", "не указан")

    mood = get_market_mood()

    return f"""Персональный инвестиционный разбор для {name}

Профиль:
• Срок: {term}
• Сумма: {amount}
• Цель: {goal}
• Стиль: {risk}

Рынок сейчас:
• S&P 500: {market["sp500"]}
• Нефть: {market["oil"]}
• Золото: {market["gold"]}

Анализ:
Сейчас {mood}. С учётом выбранного срока и бюджета разумнее рассматривать не одну идею, а несколько сценариев с диверсификацией.

Потенциал:
Ориентировочный диапазон доходности может составлять 7–14% годовых, но фактический результат зависит от рыночной ситуации, комиссий брокера и выбранных инструментов.

Риски:
Возможна краткосрочная волатильность. Особенно важно не вкладывать всю сумму в один актив и не принимать решения только на эмоциях или новостях.

Важно:
Этот материал носит информационно-образовательный характер и не является индивидуальной инвестиционной рекомендацией."""


