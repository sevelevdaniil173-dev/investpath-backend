@app.route("/")
def home():
    return "InvestPath backend is running"


@app.route("/analyze", methods=["POST"])
def analyze():
    data = request.get_json() or {}
    risk = data.get("risk", "Сбалансированный")

    market = get_market_data()
    analysis = build_analysis(data, market)

    return jsonify({
        "analysis": analysis,
        "variants": get_variants(risk),
        "scenarios": get_scenarios(risk),
        "pro_offer": {
            "title": "Нужно больше вариантов?",
            "text": "В PRO-версии доступны расширенные сценарии, больше портфелей и более подробный план действий."
        }
    })


