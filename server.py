def build_analysis(data):
    name = data.get("name", "инвестора")
    term = data.get("term", "")
    amount = data.get("amount", "")
    goal = data.get("goal", "")
    risk = data.get("risk", "")

    market = get_market_context(risk)

    return f"""Персональный инвестиционный разбор для {name}

Профиль:
• Срок: {term}
• Сумма: {amount}
• Цель: {goal}
• Стиль: {risk}

Анализ:
{market}

Выбранный подход выглядит логично с учётом ваших параметров и может использоваться как базовый сценарий входа в инвестиции.

Важно:
Этот материал носит информационный характер и не является инвестиционной рекомендацией.
"""


