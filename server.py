def get_return_range(risk, term):
    if risk == "Консервативный":
        if "5" in term or "месяц" in term:
            return "6–10% годовых"
        return "7–11% годовых"

    if risk == "Сбалансированный":
        if "5" in term or "месяц" in term:
            return "8–14% годовых"
        return "9–15% годовых"

    if "5" in term or "месяц" in term:
        return "12–20% годовых"
    return "14–22% годовых"


